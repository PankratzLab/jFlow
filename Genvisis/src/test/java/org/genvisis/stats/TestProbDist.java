	@Test
	public void testChiDistAgainstExcel() {
		for (int i = 1; i < 70; i++) {
			double truthP = EXCEL_1_DF_CHI_SQUARE_P.get(i);
			double testP = ProbDist.ChiDist(i, 1);

			chiSquareAssertions(testP, truthP, i, 1);
		}


	}

	private void chiSquareAssertions(double testP, double truthP, int chiSquare, int df) {
		// Assert.assertEquals("For " + df + " df and a Chi Square of " + chiSquare + ", ", truthP,
		// testP, 0.00001);
		if (truthP != 0.0) {
			Assert.assertTrue("For " + df + " df and a Chi Square of " + chiSquare + ", expected:<~"
												+ truthP + "> but was:<" + testP + ">",
												Math.abs(Math.log10(truthP) - Math.log10(testP)) < 1);
		}
		Assert.assertTrue("For " + df + " df and a Chi Square of " + chiSquare
											+ ", expected non-negative value but was:<" + testP + ">",
											testP >= 0.0);
	}

